named_color_hex* GetNamedColor(color_table* ColorTable, const c8* ColorName)
{
  red_black_tree_node_data* TreeData = FindData(&ColorTable->ColorTree, utils::djb2_hash(ColorName));
  if(TreeData)
  {
    return (named_color_hex*) TreeData->Data;
  }
  return 0;
}

v4 GetColor(color_table* ColorTable, const c8* ColorName)
{
  named_color_hex* Color = GetNamedColor(ColorTable, ColorName);
  if(Color)
  {
    return HexCodeToColorV4(Color->Color);
  }
  return V4(0,0,0,0);
}

named_color_hex* GetNamedColor(color_table* ColorTable, umm Index)
{
  umm WrappedIndex = Index % ColorTable->ColorCount;
  named_color_hex* Result = &ColorTable->ColorArray[WrappedIndex];
  return Result;
}

v4 GetColor(color_table* ColorTable, umm Index)
{
  named_color_hex* Color = GetNamedColor(ColorTable, Index);
  return HexCodeToColorV4(Color->Color);
}

